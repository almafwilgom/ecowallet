alter table public.waste_submissions enable row level security;
alter table public.withdrawal_requests enable row level security;

-- Policies

-- Users
drop policy if exists "Public read access" on public.users;
create policy "Public read access" on public.users for select using (true);
drop policy if exists "User update own" on public.users;
create policy "User update own" on public.users for update using (auth.uid() = id);

-- Wallets
drop policy if exists "View own wallet" on public.wallets;
create policy "View own wallet" on public.wallets for select using (auth.uid() = user_id);

-- Waste Submissions
drop policy if exists "View own submissions" on public.waste_submissions;
create policy "View own submissions" on public.waste_submissions for select using (auth.uid() = user_id);
drop policy if exists "Agent view all" on public.waste_submissions;
create policy "Agent view all" on public.waste_submissions for select using (
  exists (select 1 from public.users where id = auth.uid() and role in ('agent', 'admin'))
);
drop policy if exists "User insert own" on public.waste_submissions;
create policy "User insert own" on public.waste_submissions for insert with check (auth.uid() = user_id);
drop policy if exists "Agent update" on public.waste_submissions;
create policy "Agent update" on public.waste_submissions for update using (
  exists (select 1 from public.users where id = auth.uid() and role in ('agent', 'admin'))
);

-- Withdrawal Requests
drop policy if exists "View own withdrawals" on public.withdrawal_requests;
create policy "View own withdrawals" on public.withdrawal_requests for select using (auth.uid() = user_id);
drop policy if exists "Admin view all withdrawals" on public.withdrawal_requests;
create policy "Admin view all withdrawals" on public.withdrawal_requests for select using (
  exists (select 1 from public.users where id = auth.uid() and role = 'admin')
);
drop policy if exists "User insert withdrawal" on public.withdrawal_requests;
create policy "User insert withdrawal" on public.withdrawal_requests for insert with check (auth.uid() = user_id);
drop policy if exists "Admin update withdrawal" on public.withdrawal_requests;
create policy "Admin update withdrawal" on public.withdrawal_requests for update using (
  exists (select 1 from public.users where id = auth.uid() and role = 'admin')
);
